package delivery;

public class Customer {
	
	int customerId;
	String name;
	String address;
	String phone;
	String email;
	
	public Customer(int customerId, String name, String address, String phone, String email) {
		this.customerId = customerId;
		this.name = name;
		this.address = address;
		this.phone = phone;
		this.email = email;
	}
	
}
